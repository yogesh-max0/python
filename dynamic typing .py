# DYNAMIC TYPING
# x is assigned to an integer object
x = 42          
print(type(x))  # Output: <class 'int'>

# x is reassigned to a string object
x = "Hello"     
print(type(x))  # Output: <class 'str'>

# x is reassigned to a list object
x = [1, 2, 3]   
print(type(x))  # Output: <class 'list'>


